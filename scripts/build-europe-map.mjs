// Builds embedded SVG country paths for EU-27 + EEA from eurostat NUTS geojson.
// Mercator projection, clipped to a European bounding box, simplified.
// Output: src/lib/constants/europe-geo.ts  (no runtime fetch needed)
import fs from 'fs'

const SRC = '/tmp/europe-nuts.json'
const OUT = '/Users/tayyub/mica-license-tracker/src/lib/constants/europe-geo.ts'

const KEEP = new Set([
  'AT','BE','BG','CY','CZ','DE','DK','EE','ES','FI','FR','GR','HR','HU','IE','IT',
  'LT','LU','LV','MT','NL','PL','PT','RO','SE','SI','SK','NO','IS','LI',
])

// Clip box (lon/lat) to keep the European mainland view tight.
const BOX = { minLon: -25, maxLon: 35, minLat: 34, maxLat: 71.5 }
const VW = 1000, VH = 1150, PAD = 16

// Web Mercator: both axes in radians so x and y share one scale (conformal).
function mercX(lon) {
  return (lon * Math.PI) / 180
}
function mercY(lat) {
  const r = (lat * Math.PI) / 180
  return Math.log(Math.tan(Math.PI / 4 + r / 2))
}

// eurostat uses EL for Greece, UK for United Kingdom.
const CODE_MAP = { EL: 'GR' }
const outCode = (id) => CODE_MAP[id] ?? id

const data = JSON.parse(fs.readFileSync(SRC, 'utf8'))

// Pre-compute projected bounds over kept, clipped points.
let minX = Infinity, maxX = -Infinity, minY = Infinity, maxY = -Infinity
function inBox(lon, lat) {
  return lon >= BOX.minLon && lon <= BOX.maxLon && lat >= BOX.minLat && lat <= BOX.maxLat
}
const feats = data.features.filter((f) => KEEP.has(outCode(f.properties.id)))

for (const f of feats) {
  const polys = f.geometry.type === 'Polygon' ? [f.geometry.coordinates] : f.geometry.coordinates
  for (const poly of polys) {
    for (const ring of poly) {
      for (const [lon, lat] of ring) {
        if (!inBox(lon, lat)) continue
        const x = mercX(lon), y = mercY(lat)
        if (x < minX) minX = x; if (x > maxX) maxX = x
        if (y < minY) minY = y; if (y > maxY) maxY = y
      }
    }
  }
}

const sx = (VW - PAD * 2) / (maxX - minX)
const sy = (VH - PAD * 2) / (maxY - minY)
const s = Math.min(sx, sy)
// center
const ox = PAD + (VW - PAD * 2 - (maxX - minX) * s) / 2
const oy = PAD + (VH - PAD * 2 - (maxY - minY) * s) / 2

function project(lon, lat) {
  const x = ox + (mercX(lon) - minX) * s
  const y = oy + (maxY - mercY(lat)) * s // invert Y for screen
  return [Math.round(x * 10) / 10, Math.round(y * 10) / 10]
}

// Polygon area (screen space) to drop tiny islands.
function ringArea(pts) {
  let a = 0
  for (let i = 0; i < pts.length; i++) {
    const [x1, y1] = pts[i]
    const [x2, y2] = pts[(i + 1) % pts.length]
    a += x1 * y2 - x2 * y1
  }
  return Math.abs(a) / 2
}

function clipRing(ring) {
  // keep points inside box; if a ring is entirely outside, skip
  const pts = ring.filter(([lon, lat]) => inBox(lon, lat))
  return pts
}

const out = {}
for (const f of feats) {
  const code = outCode(f.properties.id)
  const polys = f.geometry.type === 'Polygon' ? [f.geometry.coordinates] : f.geometry.coordinates
  // Project every polygon's outer ring; keep area so we can always retain the largest.
  const built = []
  for (const poly of polys) {
    const outer = clipRing(poly[0])
    if (outer.length < 3) continue
    const projected = outer.map(([lon, lat]) => project(lon, lat))
    const area = ringArea(projected)
    const d = 'M' + projected.map(([x, y]) => `${x},${y}`).join('L') + 'Z'
    built.push({ d, area })
  }
  if (!built.length) continue
  built.sort((a, b) => b.area - a.area)
  // Always keep the largest polygon (so tiny states survive); keep others above threshold.
  const subpaths = built.filter((p, i) => i === 0 || p.area > 8).map((p) => p.d)
  out[code] = subpaths.join('')
}

const body = `// AUTO-GENERATED by scripts/build-europe-map.mjs. Do not edit by hand.
// Source: eurostat NUTS 2021 (nutsrg_0, 60M), Mercator, clipped to Europe.
export const MAP_VIEWBOX = '0 0 ${VW} ${VH}'
export const COUNTRY_PATHS: Record<string, string> = {
${Object.entries(out).map(([k, v]) => `  ${k}: '${v}',`).join('\n')}
}
`
fs.writeFileSync(OUT, body)
console.log(`Wrote ${Object.keys(out).length} country paths to europe-geo.ts`)
console.log('Missing from KEEP:', [...KEEP].filter((c) => !out[c]).join(', ') || 'none')
console.log('viewBox', VW, VH, '· file size', (body.length / 1024).toFixed(1), 'KB')
